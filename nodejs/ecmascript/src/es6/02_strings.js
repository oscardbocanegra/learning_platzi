let hello = 'Hello';
let world = 'World';
let helloWorld = hello + ' ' + world + '!';
console.log(helloWorld); // Output: Hello World!

// template literals
let epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2); // Output: Hello World!

// Multiline strings
let lorem = 'Esto es un string \n' + 'esto es otra linea';
console.log(lorem); // Output: Esto es un string \nesto es otra linea

let lorem2 = `Esta es una frase epica
  la contunuacion de la frase epica`;
console.log(lorem2); // Output: Esta es una frase epica la contunuacion de la frase epica