/**
 * Calculates the square of a number.
 * 
 * param {number} num - The number to be squared.
 * returns {number} The square of the input number.
 */


function square(num) {
    return num * num;
}

/**
 * Calculates the square of a number using an arrow function.
 * 
 * param {number} num - The number to be squared.
 * returns {number} The square of the input number.
 */


const square = (num) => {
    return num * num;
};

/**
 * Calculates the square of a number using a concise arrow function.
 * 
 * param {number} num - The number to be squared.
 * returns {number} The square of the input number.
 */


const squareArrow = (num) => num * num;

